//TIM2 定时器中断函数
void TIM2_IRQHandler(void)   //TIM3中断
{
	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET) //检查指定的TIM中断发生与否:TIM 中断源 
		{
			GPIO_ResetBits(LED_GPIO_Port, LED_GPIO_Pin);
			TIM_ClearITPendingBit(TIM2, TIM_IT_Update);  //清除TIMx的中断待处理位:TIM 中断源 
			
		}
}
//TIM3 定时器中断函数
void TIM3_IRQHandler(void)   //TIM3中断
{
static u8 countTim3=0;
	
	if (TIM_GetITStatus(TIM3, TIM_IT_Update) != RESET) //检查指定的TIM中断发生与否:TIM 中断源 
		{
			switch(countTim3)
			{
				case 0:GPIO_ResetBits(LED_GPIO_Port, LED_GPIO_Pin);countTim3++;break;
				case 1:GPIO_SetBits(LED_GPIO_Port, LED_GPIO_Pin);countTim3=0;break;
			}
			TIM_ClearITPendingBit(TIM3, TIM_IT_Update);  //清除TIMx的中断待处理位:TIM 中断源 
			
		}
}

//USART1中断函数
void USART1_IRQHandler(void)	//USART1中断
{
	u16 tap;
	if(USART_GetITStatus(USART1,USART_IT_RXNE)!=RESET)
	{
		tap=USART_ReceiveByte(USART1);
	}
	USART_SendData(USART1,tap);
}


